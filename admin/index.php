                    // Get the current year
                    const currentYear = new Date().getFullYear();

                    // Make an AJAX request to fetch monthly sales data for the current year
                    // Adjust this URL according to your server-side endpoint
                    const url = `fetch_monthly_sales_data.php?year=${currentYear}`;

                    // Fetch the data using AJAX
                    fetch(url)
                        .then(response => {
                            if (!response.ok) {
                                throw new Error('Network response was not ok');
                            }
                            return response.json();
                        })
                        .then(data => {
                            // Once data is fetched successfully, update the chart
                            updateSalesChart(data);
                        })
                        .catch(error => {
                            console.error('There was a problem with the fetch operation:', error);
                        });

                    // Function to update the sales chart with new data
                    function updateSalesChart(monthlySalesData) {
                        // Get the canvas element
                        const salesCanvas = document.getElementById('salesChart');

                        // Create the chart
                        const salesChart = new Chart(salesCanvas, {
                            type: 'line',
                            data: {
                                // Use the keys (month names) of the monthlySalesData object as labels
                                labels: Object.keys(monthlySalesData),
                                datasets: [{
                                    label: 'Monthly Sales',
                                    data: Object.values(monthlySalesData), // Use the values (sales amounts) of the monthlySalesData object as data points
                                    backgroundColor: 'rgba(75, 192, 192, 0.2)',
                                    borderColor: 'rgba(75, 192, 192, 1)',
                                    borderWidth: 1
                                }]
                            },
                            options: {
                                scales: {
                                    yAxes: [{
                                        ticks: {
                                            beginAtZero: true
                                        }
                                    }]
                                }
                            }
                        });
